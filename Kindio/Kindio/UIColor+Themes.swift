import UIKit

extension UIColor {
    class func redLust() -> UIColor {
        return UIColor.init(red: 0.9, green: 0.13, blue: 0.09, alpha: 1.0)
    }
    
    class func robinEggBlue() -> UIColor {
        return UIColor.init(red: 0, green: 0.76, blue: 0.76, alpha: 1.0)
    }
    
    class func deepTeal() -> UIColor {
        return UIColor.init(red: 0.0, green: 0.19, blue: 0.19, alpha: 1.0)
    }
    
    class func blueCharcoal() -> UIColor {
        return UIColor.init(red: 0.15, green: 0.16, blue: 0.19, alpha: 1.0)
    }
    
    class func mantis() -> UIColor {
        return UIColor.init(red: 0.49, green: 0.82, blue: 0.27, alpha: 1.0)
    }
}